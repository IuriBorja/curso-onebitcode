/*var ela é criada no escopo da função em que ela é declarada, 
ou no escopo global se ela for declarada fora de uma função. */

var nome = "Iuri";
console.log(nome);


/* Quando uma variável é declarada com let, ela é criada no escopo do bloco
de código em que ela é declarada. Isso significa que uma variável declarada 
com let dentro de um if ou de um for só estará disponível dentro desse bloco. */


let nome1 = "Marques";
console.log(nome1);


/* const é similar a let, mas é usado para declarar variáveis
que não podem ser reatribuídas. */


const nome2 = "Borja";
console.log(nome2);
